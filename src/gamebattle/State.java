package gamebattle;

public class State {

    private int healthPoints;
    private int damage;

    public int getHealthPoints() {
        return healthPoints;
    }

    public void setHealthPoints(int hp) {
        this.healthPoints = hp;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int dmg) {
        this.damage = dmg;
    }

}
